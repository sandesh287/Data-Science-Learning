# Create Vectors and Matrices using NumPy

import numpy as np

# create matrices
A = np.array([[1, 2], [3, 4]])
B = np.array([[9, 8], [7, 6]])

# element-wise operation 
# addition
print("Addition: \n", A + B)

# subtraction
print("Subtraction: \n", B - A)

# scalar multiplication
print("Scalar multiplication: \n", 3 * A)